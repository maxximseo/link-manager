/**
 * Detect current language from URL or localStorage or browser
 * Priority: 1. URL path (/en/...) 2. localStorage 3. Browser language 4. default 'en'
 * @returns {'ru' | 'en'}
 */
function getLang() {
  const path = window.location.pathname;

  // 1. Check URL path first (for landing/login pages)
  if (path.startsWith('/en/') || path === '/en') {
    return 'en';
  }

  // 2. Check localStorage (user preference saved)
  const storedLang = localStorage.getItem('lang');
  if (storedLang === 'en' || storedLang === 'ru') {
    return storedLang;
  }

  // 3. Auto-detect by browser language (RU/UK ‚Üí ru, else ‚Üí en)
  const browserLang = navigator.language || navigator.userLanguage || '';
  if (browserLang.startsWith('ru') || browserLang.startsWith('uk')) {
    return 'ru';
  }

  // 4. Default to English for all other languages
  return 'en';
}

/**
 * Set language and save to localStorage
 * @param {'ru' | 'en'} lang
 */
function setLang(lang) {
  if (lang === 'en' || lang === 'ru') {
    localStorage.setItem('lang', lang);
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    // Re-apply translations
    applyTranslations();
  }
}

/**
 * Get translated string by key
 * @param {string} key - Translation key
 * @returns {string} - Translated string or key if not found
 */
function t(key) {
  const lang = getLang();
  return translations[lang][key] || translations['ru'][key] || key;
}

/**
 * Switch to opposite language (for landing pages with URL-based routing)
 * Redirects to the correct URL
 */
function switchLanguage() {
  const currentLang = getLang();
  const path = window.location.pathname;

  if (currentLang === 'ru') {
    // Switch to English
    if (path === '/') {
      window.location.href = '/en/';
    } else if (path === '/login') {
      window.location.href = '/en/login';
    } else {
      window.location.href = '/en' + path;
    }
  } else {
    // Switch to Russian
    if (path === '/en/' || path === '/en') {
      window.location.href = '/';
    } else if (path === '/en/login') {
      window.location.href = '/login';
    } else {
      window.location.href = path.replace('/en/', '/').replace('/en', '/');
    }
  }
}

/**
 * Toggle language for dashboard pages (saves to localStorage and reloads)
 * Used by language switcher button in sidebar
 */
function toggleLanguage() {
  const currentLang = getLang();
  const newLang = currentLang === 'ru' ? 'en' : 'ru';
  localStorage.setItem('lang', newLang);
  // Reload page to apply all translations including sidebar
  window.location.reload();
}

/**
 * Get login URL for current language
 * @returns {string}
 */
function getLoginUrl() {
  return getLang() === 'en' ? '/en/login' : '/login';
}

/**
 * Get landing URL for current language
 * @returns {string}
 */
function getLandingUrl() {
  return getLang() === 'en' ? '/en/' : '/';
}

/**
 * Apply translations to all elements with data-i18n attribute
 * Usage: <span data-i18n="heroTitle1"></span>
 */
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });

  // Also apply to placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
}

// Auto-apply translations when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
  applyTranslations();
}
